let productDetailImage2 = document.querySelector('.img-thumbnail')
let productDetailTitle2 = document.querySelector('.product-detail-name')
let productDetailSeller2 = document.querySelector('.product-detail-seller')
let productDetailFeatures2 = document.querySelector('.product-detail-features')
let productDetailPrice2 = document.querySelector('.product-detail-price')

const seciliUrun2 = (index) => {
    localStorage.setItem('urunTitle', products[index].title)
    localStorage.setItem('urunPrice', products[index].price)
    localStorage.setItem('urunImage', products[index].thumbnail)
}

window.addEventListener('DOMContentLoaded', function () {
	addElement()
	if(this.localStorage.getItem('urunTitle') && this.localStorage.getItem('urunPrice') && this.localStorage.getItem('urunImage')){
        productDetailTitle2.innerText = this.localStorage.getItem('urunTitle')
        productDetailPrice2.innerText = this.localStorage.getItem('urunPrice')
        productDetailImage2.src = this.localStorage.getItem('urunImage')
    	productDetailFeatures2.innerText = sliderArr[Math.floor(Math.random() * sliderArr.length-1)]
    }
})